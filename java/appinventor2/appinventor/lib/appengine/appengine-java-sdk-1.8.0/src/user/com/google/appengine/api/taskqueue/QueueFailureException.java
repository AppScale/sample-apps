package com.google.appengine.api.taskqueue;

/**
 * Unspecified queue failure.
 *
 */
public class QueueFailureException extends RuntimeException {
  public QueueFailureException(String detail) {
    super(detail);
  }

  public QueueFailureException(String string, Throwable exception) {
    super(string, exception);
  }

  public QueueFailureException(Throwable exception) {
    super(exception);
  }
}
