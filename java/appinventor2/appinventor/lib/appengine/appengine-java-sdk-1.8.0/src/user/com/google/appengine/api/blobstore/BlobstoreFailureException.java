package com.google.appengine.api.blobstore;

/**
 * {@code BlobstoreFailureException} is an unchecked exception that is thrown
 * for any unexpected error that occurs while communicating with the
 * blobstore.
 *
 */
public class BlobstoreFailureException extends RuntimeException {
  public BlobstoreFailureException(String message) {
    super(message);
  }

  public BlobstoreFailureException(String message, Throwable cause) {
    super(message, cause);
  }
}
